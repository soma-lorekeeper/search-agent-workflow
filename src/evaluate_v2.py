def main() -> None:
    for name, question, ground_truth in NEW_QUERIES:
        print(f"===== {name} =====")
        print("Q:", question)
        print("GT:", ground_truth)
        answer = ask(question, thread_id=f"{name}-eval")
        print("A:", answer)
        print()


if __name__ == "__main__":
    main()
